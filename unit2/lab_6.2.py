print('Podaj pierwszą liczbę całkowitą: ')

liczba_jeden = int(input())

print('Podaj drugą liczbę całkowitą: ')

liczba_dwa = int(input())

print("Wynik dodawania to: " + str(liczba_dwa + liczba_jeden) )
print('Wynik odejmowania to: ' + str(liczba_jeden - liczba_dwa))
print("Wynik mnożenia to: " + str(liczba_jeden * liczba_dwa))
print("Wynik dzielenia to: " + str(liczba_jeden / liczba_dwa))
print("Wynik operacji modulo to: " + str(liczba_jeden % liczba_dwa))